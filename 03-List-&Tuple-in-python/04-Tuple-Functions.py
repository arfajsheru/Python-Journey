# Creata a simple tuple
my_tuple = (10,20,30,40,50,10,10)

# 1. count(x): Returns the number of times x appears in the tuple.
print(f"Count Function: {my_tuple.count(10)}") # Output: 3

# 2. index(x): Returns the index of the first occurrence of element x.
print(f"Index Function: {my_tuple.index(20)}") # Output: 3
