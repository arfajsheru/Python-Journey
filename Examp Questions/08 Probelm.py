# Write a program to demonstrate to string slicing

mystring = "Arfaj Mustakim sheru"

print(f"Original string: {mystring}")

print(f"First 5 character: {mystring[:5]}")

print(f"Last 5 character: {mystring[-5:]}")

print(f"Charactr from index 2 to 8: {mystring[2:9]}")

print(f"Every Second charcter in the string: {mystring[::2]}")

print(f"Reverse string: {mystring[::-1]}")

print(f"Character form index 8 to 2 reverse: {mystring[8:2:-1]}")


